import * as React from "react";
import Head from "next/head";
import Typography from "@mui/material/Typography";
import { gql, useQuery } from "@apollo/client";
import Box from "@mui/material/Box";
import Grid from "@mui/material/Unstable_Grid2";
import Cards from "../../src/components/card";
import BreadCrumb from "../../src/components/breadcrumb";
import Columns from "../../src/components/column";

const BOARD = gql`
  query ExampleQuery {
    Columns {
      id
      Title
      TaskId
    }
  }
`;

export default function Kanban() {
  const { loading, error, data } = useQuery(BOARD);

  if (loading) return "Loading...";

  if (error) return `Error! ${error.message}`;
  console.log(data.columns);

  return (
    <>
      <Head>
        <title>Kanban</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Box
        sx={{
          width: "100vw",
          height: "100vh",
          padding: "20px",
        }}
      >
        <Typography variant="h3">Kanban</Typography>
        <BreadCrumb />
        <Box
          sx={{
            mt: 2,
          }}
        >
          <Grid container columnSpacing={{ xs: 1, sm: 2, md: 3 }}>
            {data?.Columns?.map((board: any) => (
              <Grid xs={6} md={3} key={board?.id}>
                <Columns column={board} />
              </Grid>
            ))}
            {/* Display the add column only if there less than 5 columns present */}
            {(!data?.Columns || data?.Columns?.length < 5) && (
              <Grid xs={6} md={3} key="*">
                <Cards column />
              </Grid>
            )}
          </Grid>
        </Box>
      </Box>
    </>
  );
}
